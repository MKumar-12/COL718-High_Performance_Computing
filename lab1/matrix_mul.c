// Function to multiply matrices
void multiplyMatrices(int A[N][N], int B[N][N], int res[N][N]) {
    for(int i = 0; i < N; i++) {
        for(int j = 0; j < N; j++) 
            res[i][j] = 0;
    }

    // Matrix multiplication
    for (int i = 0; i < N; i++) {
        for (int j = 0; j < N; j++) {
            for (int k = 0; k < N; k++) 
                res[i][j] += A[i][k] * B[k][j]; 
        }
    }
}


